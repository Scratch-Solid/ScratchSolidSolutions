#!/usr/bin/env node
/**
 * d1-to-postgres.mjs
 * ------------------------------------------------------------------
 * Converts a Cloudflare D1 (SQLite) SQL dump into PostgreSQL-compatible SQL.
 *
 * Produce the input dump with:
 *     wrangler d1 export scratchsolid-marketing-db --remote --output d1-dump.sql
 *
 * Then convert:
 *     node d1-to-postgres.mjs d1-dump.sql > marketing-postgres.sql
 *
 * Load into Postgres:
 *     psql "$DATABASE_URL" -f marketing-postgres.sql
 *
 * Design notes / why a custom converter (vs pgloader default):
 *   - SQLite INTEGER is intentionally mapped to Postgres INTEGER (int4), NOT
 *     bigint. node-postgres returns int8 as a *string*, which would break the
 *     app's numeric comparisons and `typeof id === 'number'` checks.
 *   - Timestamp columns stay TEXT in canonical `YYYY-MM-DD HH24:MI:SS` form so
 *     the application's string comparisons keep working (matches pg-d1 adapter).
 *   - SQLite cascade-delete TRIGGERs are dropped; integrity is enforced by the
 *     `ON DELETE CASCADE` foreign keys already present in the schema.
 */

import { readFileSync } from 'node:fs';

const PG_NOW = `to_char((now() at time zone 'utc'), 'YYYY-MM-DD HH24:MI:SS')`;

const input = process.argv[2];
if (!input) {
  console.error('Usage: node d1-to-postgres.mjs <d1-dump.sql> > out.sql');
  process.exit(1);
}

const raw = readFileSync(input, 'utf8');

// Split into statements on semicolons that terminate a line. SQLite dumps are
// line-oriented and do not embed unescaped semicolons inside values produced by
// `wrangler d1 export`, so this is safe for these dumps.
const lines = raw.split(/\r?\n/);

const out = [];
out.push('-- Generated by d1-to-postgres.mjs');
out.push('BEGIN;');

let buffer = '';
let inTrigger = false;
const tablesWithId = new Set();

function flush(statement) {
  let s = statement.trim();
  if (!s) return;

  // Skip SQLite-only noise.
  if (/^PRAGMA\b/i.test(s)) return;
  if (/^BEGIN\b/i.test(s) || /^COMMIT\b/i.test(s)) return;
  if (/INSERT INTO\s+["'`]?sqlite_sequence/i.test(s)) return;
  if (/^CREATE TABLE\s+["'`]?sqlite_sequence/i.test(s)) return;
  if (/^CREATE TABLE\s+["'`]?_cf_/i.test(s)) return; // D1 internal tables

  // Drop SQLite triggers (handled by FK ON DELETE CASCADE in Postgres).
  if (/^CREATE TRIGGER/i.test(s)) return;

  // Record tables that have an `id` column for sequence reset later.
  const createMatch = s.match(/^CREATE TABLE\s+(?:IF NOT EXISTS\s+)?["'`]?([A-Za-z0-9_]+)["'`]?/i);
  if (createMatch && /\bid\b/i.test(s)) {
    tablesWithId.add(createMatch[1]);
  }

  // ── DDL type/dialect translation ──────────────────────────────
  // Identity PK: keep int4, allow explicit inserts during data load.
  s = s.replace(/INTEGER\s+PRIMARY\s+KEY\s+AUTOINCREMENT/gi, 'INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY');
  s = s.replace(/\bAUTOINCREMENT\b/gi, '');
  s = s.replace(/\bREAL\b/gi, 'DOUBLE PRECISION');
  // Timestamp defaults -> TEXT canonical now.
  s = s.replace(/DEFAULT\s*\(\s*datetime\(\s*'now'\s*\)\s*\)/gi, `DEFAULT ${PG_NOW}`);
  s = s.replace(/DEFAULT\s+CURRENT_TIMESTAMP/gi, `DEFAULT ${PG_NOW}`);
  // Inline datetime modifiers inside DEFAULT or values.
  s = s.replace(
    /datetime\(\s*'now'\s*,\s*'([+-])\s*(\d+)\s+(second|seconds|minute|minutes|hour|hours|day|days|month|months|year|years)'\s*\)/gi,
    (_m, sign, amount, unit) => {
      const op = sign === '-' ? '-' : '+';
      const u = unit.endsWith('s') ? unit : `${unit}s`;
      return `to_char(((now() at time zone 'utc') ${op} interval '${amount} ${u}'), 'YYYY-MM-DD HH24:MI:SS')`;
    }
  );
  s = s.replace(/datetime\(\s*'now'\s*\)/gi, PG_NOW);

  // INSERT OR IGNORE / REPLACE
  if (/INSERT\s+OR\s+IGNORE/i.test(s)) {
    s = s.replace(/INSERT\s+OR\s+IGNORE\s+INTO/gi, 'INSERT INTO');
    s = `${s.replace(/;?\s*$/, '')} ON CONFLICT DO NOTHING`;
  } else if (/INSERT\s+OR\s+REPLACE/i.test(s)) {
    s = s.replace(/INSERT\s+OR\s+REPLACE\s+INTO/gi, 'INSERT INTO');
    s = `${s.replace(/;?\s*$/, '')} ON CONFLICT DO NOTHING`;
  }

  // Backtick identifiers -> double quotes.
  s = s.replace(/`/g, '"');

  out.push(`${s};`);
}

for (const line of lines) {
  const trimmed = line.trim();
  if (!trimmed) continue;

  // Track multi-line CREATE TRIGGER ... END; blocks so we can drop them.
  if (/^CREATE TRIGGER/i.test(trimmed)) inTrigger = true;
  if (inTrigger) {
    if (/END\s*;?\s*$/i.test(trimmed)) inTrigger = false;
    continue;
  }

  buffer += (buffer ? '\n' : '') + line;
  if (/;\s*$/.test(line)) {
    flush(buffer);
    buffer = '';
  }
}
if (buffer.trim()) flush(buffer);

// Reset IDENTITY sequences to max(id)+1 for each table that has an id column.
for (const table of tablesWithId) {
  out.push(
    `SELECT setval(pg_get_serial_sequence('"${table}"', 'id'), COALESCE((SELECT MAX(id) FROM "${table}"), 1), true);`
  );
}

out.push('COMMIT;');
process.stdout.write(out.join('\n') + '\n');
